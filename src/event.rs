// Error
// ---------------------------------------------------------------------------

/// Error classification for the hydra system.
///
/// Errors are divided into three severity levels plus a recursive
/// variant for wrapping child-task failures.
///
/// This enum is `#[non_exhaustive]` to allow adding variants without
/// a breaking change.
#[derive(Clone, Debug, PartialEq)]
#[non_exhaustive]
pub enum Error {
    /// A transient, retryable failure (e.g. network timeout, rate
    /// limit).
    Transient(String),
    /// An operation exceeded its allowed time limit.
    Timeout(String),
    /// A non-retryable, fatal failure (e.g. invalid configuration,
    /// invariant violation).
    Fatal(String),
    /// A task failed because a sub-task it depends on failed.
    TaskFailed {
        /// The identifier of the task whose failure caused this error.
        id: TaskId,
        /// The wrapped child error.
        cause: Box<Error>,
    },
}

impl std::fmt::Display for Error {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Error::Transient(msg) => write!(f, "transient: {msg}"),
            Error::Timeout(msg) => write!(f, "timeout: {msg}"),
            Error::Fatal(msg) => write!(f, "fatal: {msg}"),
            Error::TaskFailed { id, cause } => {
                write!(f, "task {id} failed: {cause}")
            }
        }
    }
}

impl std::error::Error for Error {
    fn source(&self) -> Option<&(dyn std::error::Error + 'static)> {
        match self {
            Error::TaskFailed { cause, .. } => Some(cause.as_ref()),
            _ => None,
        }
    }
}

// ---------------------------------------------------------------------------
// Per-layer event types (defined via macro for single-source doc)
// ---------------------------------------------------------------------------

macro_rules! define_layer_event {
    ($name:ident, $description:literal) => {
        #[doc = concat!("Event type for the ", $description, " layer.")]
        #[doc = concat!($description, " events correspond to layer-specific operations.")]
        ///
        /// NOTE: This type derives `Copy`. If fields are added later,
        /// ensure they are also `Copy` or remove the `Copy` derive.
        #[derive(Debug, Clone, Copy)]
        pub struct $name;
    };
}

define_layer_event!(OrchestrationEvent, "Orchestration");
define_layer_event!(RetrievalEvent, "Retrieval");
define_layer_event!(MemoryEvent, "Memory");
define_layer_event!(ContextEvent, "Context");
define_layer_event!(ExecutionEvent, "Execution");

