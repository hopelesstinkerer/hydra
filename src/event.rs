    /// heap-allocated recursive variant.
    #[test]
    fn error_clone_runtime() {
        let inner = Error::Transient("inner".into());
        let err = Error::TaskFailed {
            id: TaskId(1),
            cause: Box::new(inner),
        };
        let cloned = err.clone();
        match &cloned {
            Error::TaskFailed {
                id: TaskId(1),
                cause,
            } => {
                assert!(
                    matches!(cause.as_ref(), Error::Transient(msg) if msg == "inner"),
                    "deep clone: inner cause was not copied correctly",
                );
            }
            other => panic!("expected Error::TaskFailed, got {other:?}"),
        }
    }

    // -- TaskId round-trip ---------------------------------------------------

    /// Verify that [`TaskId`] round-trips correctly through its inner
    /// `u64`.
    #[test]
    fn task_id_round_trip() {
        let original = TaskId(42);
        let extracted = original.0;
        let reconstructed = TaskId(extracted);
        assert_eq!(original, reconstructed);
    }

    #[test]
    fn task_id_hash_consistency() {
        let a = TaskId(1);
        let b = TaskId(1);
        let mut hasher = DefaultHasher::new();
        a.hash(&mut hasher);
        let hash_a = hasher.finish();
        let mut hasher = DefaultHasher::new();
        b.hash(&mut hasher);
        let hash_b = hasher.finish();
        assert_eq!(hash_a, hash_b, "equal TaskIds must produce equal hashes");
    }
