//! Core event types for the hydra event-sourced system.
//!
//! This module defines the foundational event and error types used
//! throughout the system. All types are self-contained and do not
//! depend on the [`crate::core`] module.
//!
//! - [`TaskId`] — unique identifier for a task
//! - [`SystemEvent`] — all domain events enumerated
//! - [`Error`] — error classification
//! - Per-layer event types — type-level markers for the five
//!   architectural layers

// ---------------------------------------------------------------------------
// TaskId
// ---------------------------------------------------------------------------

/// A unique identifier for a task.
///
/// Wraps a `u64` value. Task IDs uniquely identify a task and
/// correlate events across the system.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub struct TaskId(pub(crate) u64);

impl TaskId {
    /// Create a new `TaskId` from the given raw value.
    ///
    /// Only available in tests because production code should use the
    /// atomic counter in [`execute_task`] to avoid ID collisions.
    #[cfg(test)]
    pub fn new(value: u64) -> Self {
        TaskId(value)
    }
}

impl std::fmt::Display for TaskId {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "TaskId({})", self.0)
    }
}

// ---------------------------------------------------------------------------
// SystemEvent
// ---------------------------------------------------------------------------

/// Every state transition, operation, and error in the system is
/// recorded as a [`SystemEvent`].
///
/// All task-referencing variants use a consistent `id: TaskId` field
/// for pattern-matching convenience.
///
/// This enum is `#[non_exhaustive]` to allow adding variants without
/// a breaking change.
// Manual PartialEq: guard against NaN in Evaluation scores so that
// two identical events with NaN score compare as equal.
#[derive(Clone, Debug)]
#[non_exhaustive]
pub enum SystemEvent {
    /// A task has been activated and is ready for execution.
    TaskStarted {
        /// The identifier of the task that started.
        id: TaskId,
    },
    /// The planner decomposed a goal into sub-goals.
    TaskDecomposed {
        /// The identifier of the parent task.
        id: TaskId,
        /// The sub-goals produced by planning.
        sub_goals: Vec<String>,
    },
    /// A single step within a task was completed.
    TaskStepCompleted {
        /// The identifier of the task making progress.
        id: TaskId,
        /// The zero-based step index that was completed.
        step: u32,
    },
    /// A task completed successfully.
    TaskCompleted {
        /// The identifier of the completed task.
        id: TaskId,
        /// A human-readable summary of the result.
        summary: String,
    },
    /// A task failed after exhausting all retry attempts.
    TaskFailed {
        /// The identifier of the failed task.
        id: TaskId,
        /// The error that caused the failure.
        error: Error,
    },
    /// A task is blocked waiting on a sub-task or resource.
    TaskBlocked {
        /// The identifier of the blocked task.
        id: TaskId,
        /// A human-readable reason for the block.
        reason: String,
    },
    /// The context window was compressed to manage token usage.
    ContextCompressed {
        /// The identifier of the task whose context was compressed.
        id: TaskId,
        /// Context size (in tokens or bytes) before compression.
        before_size: u64,
        /// Context size (in tokens or bytes) after compression.
        after_size: u64,
    },
    /// An artifact was produced by a layer.
    ArtifactProduced {
        /// The identifier of the task that produced the artifact.
        id: TaskId,
        /// The name of the layer that produced it (e.g. `"Memory"`).
        layer: &'static str,
        /// The artifact content.
        artifact: String,
    },
    /// A metric evaluation was performed on a task's output.
    Evaluation {
        /// The identifier of the task being evaluated.
        id: TaskId,
        /// The metric name (e.g. `"relevance"`, `"coherence"`).
        metric: String,
        /// The score produced by the evaluation.
        score: f64,
    },
    /// Feedback was generated by the system for self-improvement.
    FeedbackGenerated {
        /// The identifier of the task that triggered the feedback.
        id: TaskId,
        /// The insight or feedback text.
        insight: String,
    },
}

// Manual PartialEq so that Evaluation's f64 score uses to_bits()
// comparison (NaN == NaN) instead of the default f64 equality
// (NaN != NaN). All other variants compare field-by-field as normal.
impl PartialEq for SystemEvent {
    fn eq(&self, other: &Self) -> bool {
        match (self, other) {
            (SystemEvent::TaskStarted { id: a }, SystemEvent::TaskStarted { id: b }) => a == b,
            (
                SystemEvent::TaskDecomposed {
                    id: a,
                    sub_goals: b,
                },
                SystemEvent::TaskDecomposed {
                    id: c,
                    sub_goals: d,
                },
            ) => a == c && b == d,
            (
                SystemEvent::TaskStepCompleted { id: a, step: b },
                SystemEvent::TaskStepCompleted { id: c, step: d },
            ) => a == c && b == d,
            (
                SystemEvent::TaskCompleted { id: a, summary: b },
                SystemEvent::TaskCompleted { id: c, summary: d },
            ) => a == c && b == d,
            (
                SystemEvent::TaskFailed { id: a, error: b },
                SystemEvent::TaskFailed { id: c, error: d },
            ) => a == c && b == d,
            (
                SystemEvent::TaskBlocked { id: a, reason: b },
                SystemEvent::TaskBlocked { id: c, reason: d },
            ) => a == c && b == d,
            (
                SystemEvent::ContextCompressed {
                    id: a,
                    before_size: b,
                    after_size: c,
                },
                SystemEvent::ContextCompressed {
                    id: d,
                    before_size: e,
                    after_size: f,
                },
            ) => a == d && b == e && c == f,
            (
                SystemEvent::ArtifactProduced {
                    id: a,
                    layer: b,
                    artifact: c,
                },
                SystemEvent::ArtifactProduced {
                    id: d,
                    layer: e,
                    artifact: f,
                },
            ) => a == d && b == e && c == f,
            (
                SystemEvent::Evaluation {
                    id: a,
                    metric: b,
                    score: c,
                },
                SystemEvent::Evaluation {
                    id: d,
                    metric: e,
                    score: f,
                },
            ) => a == d && b == e && c.to_bits() == f.to_bits(),
            (
                SystemEvent::FeedbackGenerated { id: a, insight: b },
                SystemEvent::FeedbackGenerated { id: c, insight: d },
            ) => a == c && b == d,
            _ => false,
        }
    }
}

